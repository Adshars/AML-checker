import mongoose from 'mongoose';

const userSchema = new mongoose.Schema({
  email: {
    type: String,
    required: true,
    unique: true,
    trim: true,
    lowercase: true
  },
  passwordHash: {
    type: String,
    required: true
  },

  firstName: {
    type: String,
    required: true,
    trim: true
  },
  lastName: {
    type: String,
    required: true,
    trim: true
  },

  // Relationship: User belongs to an organization
  organizationId: {
    type: mongoose.Schema.Types.ObjectId, // Organization ID in MongoDB
    ref: 'Organization',
    required: true
  },
  role: {
    type: String,
    enum: ['admin', 'user'],
    default: 'user'
  },
  createdAt: {
    type: Date,
    default: Date.now
  }
});

    // ID (._id) generuje siÄ™ automatycznie.
export default mongoose.model('User', userSchema);